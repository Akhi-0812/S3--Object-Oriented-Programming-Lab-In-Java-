import java.util.*;

 // 3.Check given number is odd or even.



public class EvenOdd {

    public static void main(String[] args) {
        int num=8;

        if(num % 2 == 0)
            System.out.println(num + " is even.");
        else
            System.out.println(num + " is odd.");
    }
}
//Output:
//8 is even.